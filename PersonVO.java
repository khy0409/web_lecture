package day02;

public class PersonVO {
	
	PersonVO() {
		
	}

	String name;
	int age;
	double height;
	String gender;
	
	
	public PersonVO(String name, int age, double height, String gender) {
		this.name = name;
		this.age = age;
		this.height = height;
		this.gender = gender;
	}
	
}
